#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((src[i] != '\0') && (i < n))
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int	main(void)
{
	char	d[21] = "Hello this is a test";
	char	s[6] = "World";

	printf("%s\n", d);
	printf("%s\n", ft_strncpy(&d[0], &s[0], 13));
	return (0);
}
