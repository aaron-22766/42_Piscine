int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
		return (0);
	while (i < nb)
	{
		nb = nb - i;
		i += 2;
	}
	if (i == nb)
		return (i / 2 + 1);
	return (0);
}
