#include <stdio.h>

char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char	dest[6] = "abd";
	char	src[30] = "";

	printf("%s\n", dest);
	printf("%s\n", ft_strcat(&dest[0], &src[0]));
	return (0);
}
