#include <stdio.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int				i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0' && j < nb)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char	dest[20] = "abhijk";
	char	src[30] = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";

	printf("%s\n", dest);
	printf("%s\n", ft_strncat(&dest[0], &src[0], 20));
	return (0);
}
