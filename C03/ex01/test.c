#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && (s1[i] != '\0' || s2[i] != '\0'))
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	return (0);
}

int	main(void)
{
	char	s1[30] = "aBCA";
	char	s2[10] = "aBCAe";

	printf("%i\n", ft_strncmp(&s1[0], &s2[0], 0));
	return (0);
}
